package MyPojo;

public class MoviesModelClass {
	
	private int movie_Id;
	private String movie_Name;
	private String director_Name;
	private String genre;
	private String release_Date;
	private String description;
	private String actor_Name;
	private String actress_name;
	private String supporting_Actor;
	private String image;
	private float ratings;
	
	public int getMovie_Id() {
		return movie_Id;
	}
	public void setMovie_Id(int movie_Id) {
		this.movie_Id = movie_Id;
	}
	public String getMovie_Name() {
		return movie_Name;
	}
	public void setMovie_Name(String movie_Name) {
		this.movie_Name = movie_Name;
	}
	public String getDirector_Name() {
		return director_Name;
	}
	public void setDirector_Name(String director_Name) {
		this.director_Name = director_Name;
	}
	public String getGenre() {
		return genre;
	}
	public void setGenre(String genre) {
		this.genre = genre;
	}
	public String getRelease_Date() {
		return release_Date;
	}
	public void setRelease_Date(String release_Date) {
		this.release_Date = release_Date;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getActor_Name() {
		return actor_Name;
	}
	public void setActor_Name(String actor_Name) {
		this.actor_Name = actor_Name;
	}
	public String getActress_name() {
		return actress_name;
	}
	public void setActress_name(String actress_name) {
		this.actress_name = actress_name;
	}
	public String getSupporting_Actor() {
		return supporting_Actor;
	}
	public void setSupporting_Actor(String supporting_Actor) {
		this.supporting_Actor = supporting_Actor;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public float getRatings() {
		return ratings;
	}
	public void setRatings(float ratings) {
		this.ratings = ratings;
	}
	
	
	
}
